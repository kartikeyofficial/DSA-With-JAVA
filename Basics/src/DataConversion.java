public class DataConversion {
    static void main(String[] args) {
        float a = 25.12f;
        int b = (int) a;
        System.out.println(b);

        char ch1 = 'a';
        char ch2 = 'b';
        int number1= ch1;
        int number2= ch2;
        System.out.println(number1);
        System.out.println(number2);
    }
}
